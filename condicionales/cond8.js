//Realizar un programa que pida tres números y diga cuáles son pares y cuáles impares. 
var num1= parseInt(prompt("inhrese un numero"));
var num2= parseInt(prompt("ingrese un numero"));
var num3= parseInt(prompt("ingrese un numero"));

(num1%2==0) ? alert(`el numero ${num1} par`) : alert(`el numero ${num1} imarpar`);
(num2%2==0) ? alert(`el numero ${num2} par`) : alert(`el numero ${num2} imarpar`);
(num3%2==0) ? alert(`el numero ${num3} par`) : alert(`el numero ${num3} imarpar`);

