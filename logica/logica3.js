//Sacar la hipotenusa de un triángulo rectángulo, pidiendo al usuario el valor de los 2 catetos. 
let cateto1= parseInt(prompt("ingrese el valor del cateto 1"));
let cateto2= parseInt(prompt("ingrese el valor del cateto 2"));
let hipotenusa = parseInt ((cateto1*cateto1)+(cateto2*cateto2));
alert(`su hipotenusa es de ${hipotenusa}`);

